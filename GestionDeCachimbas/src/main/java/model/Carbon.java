package model;

/**
 *
 * @author Fran13
 */
public class Carbon {
    private String tipo;
    private float precio;

    public Carbon(String tipo, float precio) {
        this.tipo = tipo;
        this.precio = precio;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public float getPrecio() {
        return precio;
    }

    public void setPrecio(float precio) {
        this.precio = precio;
    }
    
    
}
